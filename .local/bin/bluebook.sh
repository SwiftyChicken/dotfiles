BLUE='\033[2;36m'
WHITE='\033[0;37m'
TITLE1='\033[2;37m'
TITLE2='\033[9;37m'
RESET=$(tput sgr0)

 
echo -e "${TITLE2}                   ${RESET}[${TITLE1} E   X      M   A   C   H   I   N   A ${RESET}]${TITLE2}                   ${RESET}"
echo -e "${WHITE}   ######   #     #     #  ######   ${BLUE}######     #####      #####    #    #${RESET}"
echo -e "${WHITE}    #    #  #     #     #  #        ${BLUE} #    #   #     #    #     #   #   # ${RESET}"
echo -e "${WHITE}     #   #  #     #     #  #        ${BLUE}  #   #  #       #  #       #  #  #  ${RESET}"
echo -e "${WHITE}      #  #  #     #     #  #        ${BLUE}   #  #  #       #  #       #  # #   ${RESET}"
echo -e "${WHITE}    #####   #     #     #  ######   ${BLUE} #####   #       #  #       #  ##    ${RESET}"
echo -e "${WHITE}   #     #  #     #     #  #        ${BLUE}#     #  #       #  #       #  # #   ${RESET}"
echo -e "${WHITE}   #     #  #     #     #  #        ${BLUE}#     #  #       #  #       #  #  #  ${RESET}"
echo -e "${WHITE}   #     #  #     #     #  #        ${BLUE}#     #   #     #    #     #   #   # ${RESET}"
echo -e "${WHITE}   ######   ####   #####   ######   ${BLUE}######     #####      #####    #    #${RESET}"
echo -e "${TITLE2}               ${RESET}[${TITLE1} B   E   Y   O   N   D   S   E   A   R   C   H ${RESET}]${TITLE2}              ${RESET}"
